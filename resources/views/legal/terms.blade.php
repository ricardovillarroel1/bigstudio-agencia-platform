        <h2>1. Aceptación de los términos</h2>
        <p>Al instalar y utilizar la app <strong>Lioren Integration</strong> en su tienda Shopify, usted acepta estos términos. Si no está de acuerdo, no debe instalar la app.</p>

        <h2>2. Descripción del servicio</h2>
        <p>Lioren Integration sincroniza pedidos pagados de Shopify con el sistema de facturación electrónica chileno Lioren para emitir automáticamente boletas y facturas de venta (DTE) ante el Servicio de Impuestos Internos (SII).</p>

        <h2>3. Requisitos del comerciante</h2>
        <ul>
            <li>Contar con una tienda Shopify activa con dirección en Chile.</li>
            <li>Tener una cuenta activa en <a href="https://www.lioren.cl" target="_blank">Lioren</a> con folios CAF disponibles y certificado digital cargado.</li>
            <li>Proporcionar una API key válida de Lioren en el onboarding.</li>
            <li>Mantener al día sus obligaciones tributarias con el SII chileno.</li>
        </ul>

        <h2>4. Responsabilidades del comerciante</h2>
        <ul>
            <li>La exactitud de los datos tributarios (RUT, razón social, giro, dirección) ingresados por el cliente final en el checkout.</li>
            <li>El cumplimiento tributario propio ante el SII.</li>
            <li>Mantener vigente su plan en Lioren.</li>
            <li>Comunicar cualquier problema o discrepancia en los DTEs emitidos dentro de 30 días.</li>
        </ul>

        <h2>5. Limitación de responsabilidad</h2>
        <p>Lioren Integration es un puente entre Shopify y Lioren. No somos responsables por:</p>
        <ul>
            <li>Caídas, errores o latencias de las APIs de Shopify, Lioren o el SII.</li>
            <li>Errores en los datos de facturación ingresados por el cliente final.</li>
            <li>Multas o sanciones del SII derivadas de la operación tributaria del comerciante.</li>
            <li>Daños indirectos, lucro cesante o pérdida de datos.</li>
        </ul>

        <h2>6. Disponibilidad del servicio</h2>
        <p>Trabajamos para mantener una alta disponibilidad, pero no garantizamos servicio ininterrumpido. Podemos realizar mantenimientos programados con aviso previo cuando sea posible.</p>

        <h2>7. Precios y pagos</h2>
        <p>El precio del servicio y las condiciones de cobro se muestran en el onboarding y en el panel del comerciante antes de la activación. Cualquier cambio futuro se comunicará con al menos 15 días de anticipación.</p>

        <h2>8. Cancelación y desinstalación</h2>
        <p>Puedes desinstalar la app en cualquier momento desde el panel de Apps de Shopify. Al desinstalar:</p>
        <ul>
            <li>Dejamos de emitir DTEs inmediatamente.</li>
            <li>Cancelamos los webhooks asociados y revocamos los tokens de acceso.</li>
            <li>Eliminamos tus datos dentro de 48 horas, salvo DTEs emitidos que conservamos 6 años por obligación legal.</li>
        </ul>

